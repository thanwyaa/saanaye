function claimEdeya() {
    // غيرت المفتاح هنا لـ eid_2026_final عشان لو جربت قبل كدة يرضى يفتح معاك تاني للتجربة
    if (localStorage.getItem("eid_2026_final")) {
        alert("هي مرة واحدة بس يا بطل! الشبشب هيطير عليك لو جربت تاني 😂");
        return;
    }
    
    document.getElementById("eidModal").style.display = "flex";
    document.getElementById("loadingView").style.display = "block";
    document.getElementById("resultView").style.display = "none";

    setTimeout(() => {
        const pick = gifts[Math.floor(Math.random() * gifts.length)];
        document.getElementById("loadingView").style.display = "none";
        document.getElementById("resultView").style.display = "block";
        document.getElementById("resIcon").innerText = pick.i;
        document.getElementById("resPrize").innerText = pick.t;
        document.getElementById("resMsg").innerText = pick.m;
        
        localStorage.setItem("eid_2026_final", "true");
        
        // تغيير نص الزرار الأصلي لو موجود
        const btn = document.getElementById("giftBtn");
        if(btn) btn.innerText = "عيدك مبارك ❤️";
    }, 1500);
}
